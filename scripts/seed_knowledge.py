def seed():
    client = db.admin_client()

    # Clear existing data
    try:
        client.table("knowledge_base").delete().neq("id", "00000000-0000-0000-0000-000000000000").execute()
        print("Cleared existing knowledge_base data")
    except Exception as e:
        print(f"Warning: Could not clear table: {e}")
        print("Make sure to run the migration SQL first!")
        return

    total = len(KNOWLEDGE)
    success = 0
    for i, item in enumerate(KNOWLEDGE):
        print(f"[{i+1}/{total}] Inserting: {item['topic']}...")

        try:
            client.table("knowledge_base").insert({
                "category": item["category"],
                "topic": item["topic"],
                "content": item["content"],
            }).execute()
            success += 1
            print(f"  ✓ Inserted")
        except Exception as e:
            print(f"  ✗ Error: {e}")

    print(f"\nDone! Seeded {success}/{total} knowledge items.")


if __name__ == "__main__":
    seed()
